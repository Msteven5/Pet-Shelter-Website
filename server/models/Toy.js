const mongoose = require('mongoose');

const { Schema } = mongoose;

const toySchema = new Schema({
    name: {
        type: String,
        required: true
    },
    type: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    quantity: {
        type: Number,
        required: true,
        default: 0
    }
});

const Toy = mongoose.model('Toy', toySchema)

module.exports = Toy;